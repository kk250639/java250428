package ch03.lecture;

public class C07Double {
    public static void main(String[] args) {
        // 실수(double)는 근삿값으로 저장됨
        double a = 0.1;
        double b = 0.2;
        System.out.println("a = " + a);
        System.out.println("b = " + b);

        double c = a + b;
        System.out.println("c = " + c);

        // 수억명의 1원이 모이면 큰 차이 그 떄는 회사 내규를 따르기
    }
}
